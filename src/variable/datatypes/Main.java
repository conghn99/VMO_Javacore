package variable.datatypes;

public class Main {
    public static void main(String[] args) {
        // Primitive type
        byte numByte = 1;
        short numShort = 10;
        int numInt = 1000;
        long numLong = 100000;
        float numFloat = 500f;
        double numDouble = 10000d;
        char word = 'a';
        boolean isTrue = true;

        // Reference type: class, interface, collection, string, arr,..
        String str = "this is a string";
    }
}
